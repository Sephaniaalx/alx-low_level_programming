#include "main.h"
#include <unistd.h>
#include <stdio.h>

/**
 * print_alphabet - printing x10 of the alphabet in lower case
 *
 * Return: Always 0
 */

void print_alphabet_x10(void)
{
int i;
char alphabet;
for (i = 0; i <= 10; i++)
{
for (alphabet = 'a'; alphabet <= 'z'; alphabet++)
{
_putchar(alphabet);
}
{
while (alphabet = 'z')
{
_putchar('\n');
}
}
}
}
